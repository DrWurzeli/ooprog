package aufgabe1_2;

public class Person {
	private String vorname;
	private String nachname;
	
	public void setVorname(String v) {
		vorname = v;
	}
	
	public void setNachname(String n) {
		nachname = n;
	}
	
	public String getVorname() {
		return vorname;
	}
	
	public String getNachname() {
		return nachname;
	}
}
