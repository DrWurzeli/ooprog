package aufgabe2;

public class Person {
	private String vorname;
	private String name;
	
	public void setVorname(String vorname) {
		this.vorname = vorname;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getVorname() {
		return vorname;
	}
	
	public String getName() {
		return name;
	}
}
